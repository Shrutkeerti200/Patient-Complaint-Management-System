    private void addNotificationForSelectedCategory() {
        String drugName;
        int selectedRow = drugCatalogTable.getSelectedRow(); // Get the selected row index

        if (selectedRow != -1) { // Ensure a row is selected
           DefaultTableModel model = (DefaultTableModel) drugCatalogTable.getModel();
          drugName = (String) model.getValueAt(selectedRow, 1); // Column index 1 for DrugName
          //System.out.println("Selected Drug Name: " + drugName);
        } else {
         JOptionPane.showMessageDialog(this, "Please select a Drug first.", "Warning", JOptionPane.WARNING_MESSAGE);

            return;
        }

    // Get the current timestamp
    String currentTimestamp = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss").format(new Date());

    // Create a new notification
    Notification notification = new Notification(
        "Increase Stock",                        // Status
        currentTimestamp,                   // Timestamp
        authenticatedUser.getRole(),        // FromRole
        "Admin",                            // ToRole
        drugName                   // Regarding
    );

    // Add the notification to the NotificationDirectory
    notificationDirectory.addNewNotification(notification);

    // Confirmation message
    JOptionPane.showMessageDialog(this, "Request sent to Admin to Increase the stock", "Notification", JOptionPane.INFORMATION_MESSAGE);
        
    }
