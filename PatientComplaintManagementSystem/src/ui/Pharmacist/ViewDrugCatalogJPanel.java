    private void populateDrugCatalogTable() {
        DefaultTableModel model = (DefaultTableModel) drugCatalogTable.getModel();
        model.setRowCount(0);

        for (Drug drug : drugCatalog.getDrugList()) {
            Object[] row = new Object[4];
            row[0] = drug.getDrugId();
            row[1] = drug.getDrugName();
            row[2] = drug.getPrice();
            row[3] = drug.getAvailablility();
            model.addRow(row);
        }
    }

